public class StringExercise {
    public static void main(String[] args){
        String noEducation = "We don't need no education \nWe don't need no thought control";
        System.out.println(noEducation);

        String escape = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(escape);

        String doubleBackslash = "In windows, the main drive is usually C:\\";
        System.out.println(doubleBackslash);

        String backslashGalore = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(backslashGalore);
    }
}
